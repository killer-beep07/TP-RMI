    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int ligne = machineList.getSelectedRow();

        if (ligne == -1) {
            JOptionPane.showMessageDialog(null, "Séléctionnez une machine !");
        } else {
            try {
                String ref = txtRef.getText();
                String marque = txtMarque.getText();
                Double prix = Double.parseDouble(txtPrix.getText());
                Salle salle = dao2.findByCode(sallecombox.getSelectedItem().toString());
                Machine ml = new Machine(ref, marque, prix, salle);

                if (dao.update(dao.findById(id), ml)) {
                    JOptionPane.showMessageDialog(this, "Machine modifiée");
                    load();

                }
            } catch (RemoteException ex) {
                Logger.getLogger(MachineForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

